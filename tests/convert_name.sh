#!/usr/bin/bash

# ================================================================================
#
get_dns_name () {
    args=()
    num=0;
    for i in "$@"; do
        args[num]=$(echo -n "$i" | xxd -p )
        num+=1
    done
    echo $(printf '%s\n' "${args[@]}" | jq -R . | jq -s -c .);
}

# ================================================================================
#
get_dns_name $@
